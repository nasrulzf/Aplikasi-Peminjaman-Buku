    private void buttonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSimpanActionPerformed
        // TODO add your handling code here:
        
        if(uController.validasi()){
            
            UserDetail userD = new UserDetail(getTeksNamaDepan().getText(), getTeksNamaBelakang().getText(), 
                    getTeksEmail().getText(), getTeksNoTelp().getText(), getTeksAlamat().getText(), getChooserTgl().getDate());
            userD.setNoId(getTeksNoId().getText());
            Users users = new Users();
            users.setNoId(getTeksNoId().getText());
            users.setuTipe("MEMBER");
            users.setsAktifasi("AKTIF");
            
            System.out.println("Cek : [namadepan/namabelakang/email/telp/alamat/tanggal] : " + userD.getIdUser()  + " / "  + userD.getNamaD() + " / " + userD.getNamaB() + " / " + userD.getEmail() + " / " + userD.getTelp()+ " / " + userD.getAlamat()+ " / " + userD.getTanggalLahir());
            
            uController.simpanUser(users, userD, inputMode);
            
        }
        
    }//GEN-LAST:event_buttonSimpanActionPerformed

