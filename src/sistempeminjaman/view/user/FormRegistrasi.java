    private void setMode(int mode){
        inputMode = mode;    
        if(inputMode == AKSI_EDIT){
            getTeksNoId().setEditable(false);
            buttonCetak.setEnabled(true);
        }else{
            getTeksNoId().setEditable(true);
            buttonCetak.setEnabled(false);
            
        }
    }
    
