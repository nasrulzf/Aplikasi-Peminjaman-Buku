    private void buttonCetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonCetakActionPerformed
        // TODO add your handling code here:
        
        if(user == null) JOptionPane.showMessageDialog(this, "Error, kesalahan di Objek user");
        
        Map dataReport = new HashMap();
        dataReport.put("P_NOID", user.getNoId());
        
        InputStream dataSource = null;
        String dataDest = null;
        
        com.mysql.jdbc.Connection c = (com.mysql.jdbc.Connection) Koneksi.getConnection();
        
        try{

            dataSource = getClass().getResourceAsStream("/sistempeminjaman/laporan/kartu_anggota.jrxml");
            
            //System.out.println(System.getProperty("user.dir"));
            
            //dataDest = getClass().getResource("/org/sijadwal/laporan/laporanPerKelas.jasper").getFile();
            //dataSource = System.getProperty("user.dir") + "/src/org/sijadwal/laporan/laporanPerKelas.jrxml";
            //dataDest = System.getProperty("user.dir") + "/src/org/sijadwal/laporan/laporanPerKelas.jasper";
            
            //System.getenv(dataDest);
            
            JasperReport jasperReport = JasperCompileManager.compileReport(dataSource);
            JasperPrint jasperPrint = JasperFillManager.fillReport(jasperReport, dataReport, c);
            //JasperExportManager.exportReportToPdfStream(jasperPrint, null);
            //JasperExportManager.exportReportToHtmlFile(jasperPrint, dataDest);
            JasperViewer.viewReport(jasperPrint, false);
            
            uController.closeListUser();
            dispose();
            
        }catch(JRException ex){
            
            //JOptionPane.showMessageDialog(this, ex);
            
            JOptionPane.showMessageDialog(null, "Error : " + ex.getMessage());
            
            Logger.getLogger(FormRegistrasi.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_buttonCetakActionPerformed

